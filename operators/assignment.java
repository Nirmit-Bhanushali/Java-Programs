package operators;

public class assignment {
    public static void main(String[] args) {
        int x = 5;
        System.out.println(x);
        x = 5;
        x += 3;
        System.out.println(x);
        x = 5;
        x -= 3;
        System.out.println(x);
        x = 5;
        x *= 3;
        System.out.println(x);
        x = 5;
        x /= 3;
        System.out.println(x);
        x = 5;
        x %= 3;
        System.out.println(x);
        x = 5;
        x &= 3;
        System.out.println(x);
        x = 5;
        x |= 3;
        System.out.println(x);
        x = 5;
        x ^= 3;
        System.out.println(x);
        x = 5;
        x >>= 3;
        System.out.println(x);
        x = 5;
        x <<= 3;
        System.out.println(x);
    }
}
